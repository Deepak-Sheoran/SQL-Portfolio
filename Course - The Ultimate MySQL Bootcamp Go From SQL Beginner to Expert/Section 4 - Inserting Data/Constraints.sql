-- PRIMARY KEY and AUTO_INCREMENT
CREATE TABLE IF NOT EXISTS Unique_cats
	(
		Cat_Id TINYINT UNSIGNED AUTO_INCREMENT,
        Name VARCHAR(20) NOT NULL DEFAULT 'UnNamed',
        Age TINYINT UNSIGNED NOT NULL DEFAULT 0,
        PRIMARY KEY(Cat_Id)
    );
DESC Unique_cats;

